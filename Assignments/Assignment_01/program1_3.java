class Demo
{
    ////////////////////////////////////////////////////////////////////////////
    //
    //  Function Name : Display
    //  Description   : Use to print 1 to 5 in reverse order.
    //  Input         :  
    //  Output        : void
    //
    ////////////////////////////////////////////////////////////////////////////

    void Display()
    {
        int iCnt = 0;

        iCnt = 5;
        while(iCnt >= 1)
        {
            System.out.println(iCnt);
            iCnt--;
        }
    } // End of Accept
}

