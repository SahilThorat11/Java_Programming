import java.util.Scanner;

////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Display
//  Description    : Contains method to print '*' on screen
//  Author         : Sahil Rajaram Thorat
//  Date           : 17/10/2025
//
////////////////////////////////////////////////////////////////////////////////

class Display
{
    ////////////////////////////////////////////////////////////////////////////
    //
    //  Function Name : Accept
    //  Description   : Accept number and print '*' that many times
    //  Input         : Integer 
    //  Output        : void
    //
    ////////////////////////////////////////////////////////////////////////////

    void Accept(int iNo)
    {
        int iCnt = 0;

        for(iCnt = 1; iCnt <= iNo; iCnt++)
        {
            System.out.println("*");
        }
    } // End of Accept
}

////////////////////////////////////////////////////////////////////////////////
