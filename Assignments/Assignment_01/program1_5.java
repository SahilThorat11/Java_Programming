class program1_1
{
    ////////////////////////////////////////////////////////////////////////////
    //
    //  Function Name : main
    //  Description   : Entry point function which takes input from user
    //
    ////////////////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        int iValue = 0;
        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter the Number : ");
        iValue = sobj.nextInt();   

        Display dobj = new Display();  
        dobj.Accept(iValue);        

        sobj.close();
    } // End of main
}

